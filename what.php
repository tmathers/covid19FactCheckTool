	<h2>What is COVID-19?</h2>
	<p>
    COVID-19, also know as "Coronavirus", is a disease caused by emergence of SARS-CoV-2 (severe acute respiratory syndrome coronavirus 2). Coronaviruses can affect and cause illness in humans or animals. Coronaviruses are not usually transmitted between humans and animals; However, three strains of coronaviruses have been able to perform this jump: MERS, SARS, and now SARS-CoV-2. The problem found is that as the virus spreads from animal to human, the human body isn’t equipped/ready to deal with it leading to  Severe Respiratory infections.
    </p>
    
    <h2>How Does COVID-19 spread?</h2>
    <p>
    The COVID-19 virus spreads mainly via droplets or direct contact. In this context, droplets are expelled from the nose or mouth while coughing, sneezing, or speaks. Consequently, if people breathe in these droplets, they can become infected. These droplets are relatively heavy and may land on surfaces and objects, and people can catch the virus if they touch their nose, mouth or eyes after touching the contaminated surface. Some community spread is also observed, where the virus is transmitted by non-symptomatic individuals. Anyone can catch COVID-19. The elderly and immunocompromised as well as those with underlying conditions, are especially vulnerable to the virus.
    </p>

    <h2>How can You Prevent COVID-19?</h2>
    <b>-Social/Physical Distancing</b>
        <p>
        By standing a meter apart, the droplets expelled from an undiagnosed sick person or carrier, can have time to land on the ground. This prevents other people from breathing the droplets and becoming potentially infected, and prevents you from unwittingly becoming a carrier for the virus, even if you don’t get sick.
        </p>
    <b>-Hand washing</b>
        <p>
        When you wash your hands for 20 seconds with soap and water, you are penetrating the envelope that surrounds the virus, essentially killing it and washing its remnants away. This allows you to stop the spread of the virus from person to person and effectively limit the number of people who will be infected. You do not only protect yourself but others surrounding you as well.
        </p>
    <b>-Things NOT to do</b>
    <p>
    Do not drink bleach. Do not over consume things beyond the official healthy limit. Do not listen to the latest ‘article’ on boosting your immune system. If you are ever unsure about whether something will decrease your chance of catching the virus, that’s what we’re here for. Simply link the article in the search bar or type your questions and you’ll find all the results from the most accurate and reliable sources.
    </p>
    <p>

